    @Override
    public void fillFromSimu(Simulation currentSimu) {
        if(currentSimu instanceof TightBindingSimulationOld)
        {
            TightBindingSimulationOld simu = (TightBindingSimulationOld)currentSimu;
            
            jRB_model1.setSelected(simu.getModel() == 1);
            jRB_model2.setSelected(simu.getModel() == 2);
            LTextField.setText(""+simu.getL());
            MTextField.setText(""+simu.getM());
            NkptTextField.setText(""+simu.getNkpt());
        }
        else
        {
            throw new UnsupportedOperationException("Not able to tackle simulation.");
        }
    }

    @Override
    public void fillSimu(Simulation currentSimu) {
        
        if(currentSimu instanceof TightBindingSimulationOld)
        {
            TightBindingSimulationOld simu = (TightBindingSimulationOld)currentSimu;
            
            if(jRB_model1.isSelected())
            {
                simu.setModel(1);
            }
            else if(jRB_model2.isSelected())
            {
                simu.setModel(2);
            }
            simu.setL(Integer.parseInt(LTextField.getText()));
            simu.setM(Integer.parseInt(MTextField.getText()));
            simu.setNkpt(Integer.parseInt(NkptTextField.getText()));
            
        }
        else
        {
            throw new UnsupportedOperationException("Not able to tackle simulation.");
        }
        
    }

